CREATE TABLE `municipio` (
	`MUN_ID` INT(11) NOT NULL AUTO_INCREMENT,
	`MUN_NAME` VARCHAR(50) NOT NULL COLLATE 'utf8_unicode_ci',
	`FK_DTO_ID` INT(11) NOT NULL,
	`CREATED_BY` VARCHAR(50) NOT NULL COLLATE 'utf8_unicode_ci',
	`CREATED_DATE` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
	`MODIFIED_BY` VARCHAR(50) NULL DEFAULT NULL COLLATE 'utf8_unicode_ci',
	`MODIFIED_DATE` DATETIME NULL DEFAULT NULL,
	PRIMARY KEY (`MUN_ID`),
	INDEX `FK_MUNICIPIO_DEPARTAMENTO` (`FK_DTO_ID`),
	INDEX `FK_MUNICIPIO_USER` (`CREATED_BY`),
	CONSTRAINT `FK_MUNICIPIO_DEPARTAMENTO` FOREIGN KEY (`FK_DTO_ID`) REFERENCES `departamento` (`DTO_ID`),
	CONSTRAINT `FK_MUNICIPIO_USER` FOREIGN KEY (`CREATED_BY`) REFERENCES `user` (`USERNAME`)
)
COLLATE='utf8_unicode_ci'
ENGINE=InnoDB
;