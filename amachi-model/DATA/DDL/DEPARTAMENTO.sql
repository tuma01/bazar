CREATE TABLE `departamento` (
	`DTO_ID` INT(11) NOT NULL AUTO_INCREMENT,
	`DTO_NAME` VARCHAR(50) NOT NULL COLLATE 'utf8_unicode_ci',
	`FK_PYS_ID` INT(11) NOT NULL,
	`CREATED_BY` VARCHAR(50) NOT NULL COLLATE 'utf8_unicode_ci',
	`CREATED_DATE` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
	`MODIFIED_BY` VARCHAR(50) NULL DEFAULT NULL COLLATE 'utf8_unicode_ci',
	`MODIFIED_DATE` DATETIME NULL DEFAULT NULL,
	PRIMARY KEY (`DTO_ID`),
	UNIQUE INDEX `UK_DTO_NAME` (`DTO_NAME`),
	INDEX `FK_DEPARTAMENTO_USER` (`CREATED_BY`),
	INDEX `FK_DEPARTAMENTO_PAYS` (`FK_PYS_ID`),
	CONSTRAINT `FK_DEPARTAMENTO_PAYS` FOREIGN KEY (`FK_PYS_ID`) REFERENCES `pays` (`PYS_ID`),
	CONSTRAINT `FK_DEPARTAMENTO_USER` FOREIGN KEY (`CREATED_BY`) REFERENCES `user` (`USERNAME`)
)
COLLATE='utf8_unicode_ci'
ENGINE=InnoDB
;