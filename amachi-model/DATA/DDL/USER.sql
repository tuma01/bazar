CREATE TABLE `user` (
	`USER_ID` INT(11) NOT NULL AUTO_INCREMENT,
	`USERNAME` VARCHAR(50) NOT NULL COLLATE 'utf8_unicode_ci',
	`PASSWORD` VARCHAR(50) NOT NULL COLLATE 'utf8_unicode_ci',
	`FIRSTNAME` VARCHAR(50) NULL DEFAULT NULL COLLATE 'utf8_unicode_ci',
	`LASTNAME` VARCHAR(50) NULL DEFAULT NULL COLLATE 'utf8_unicode_ci',
	`EMAIL` VARCHAR(50) NOT NULL COLLATE 'utf8_unicode_ci',
	`STATUS` VARCHAR(255) NULL DEFAULT NULL COLLATE 'utf8_unicode_ci',
	PRIMARY KEY (`USER_ID`),
	UNIQUE INDEX `UK_USERNAME` (`USERNAME`),
	UNIQUE INDEX `UK_EMAIL` (`EMAIL`)
)
COLLATE='utf8_unicode_ci'
ENGINE=InnoDB
AUTO_INCREMENT=1
;

CREATE TABLE `users_roles` (
	`USERNAME` VARCHAR(50) NOT NULL COLLATE 'utf8_unicode_ci',
	`ROLE_ID` INT(11) NOT NULL,
	INDEX `FK_ROLE_ID` (`ROLE_ID`),
	INDEX `FK_USERNAME` (`USERNAME`),
	CONSTRAINT `FK_USERNAME` FOREIGN KEY (`USERNAME`) REFERENCES `user` (`USERNAME`),
	CONSTRAINT `FK_ROLE_ID` FOREIGN KEY (`ROLE_ID`) REFERENCES `role` (`ROLE_ID`)
)
COLLATE='utf8_unicode_ci'
ENGINE=InnoDB
;
